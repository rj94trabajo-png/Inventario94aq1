async function addMotoresSector3() {
  const client = await pool.connect();
  
  try {
    console.log(`Iniciando proceso para Sector 3...`);
    
    // Paso 1: Eliminar todos los motores del Sector 3
    console.log('\n🗑️  Eliminando todos los motores del Sector 3...');
    const deleteResult = await client.query(
      'DELETE FROM motores WHERE sector = $1',
      ['Sector 3']
    );
    console.log(`✅ Eliminados: ${deleteResult.rowCount} motores del Sector 3`);
    
    // Paso 2: Insertar los 200 códigos
    console.log(`\n📝 Insertando ${codigosMotores.length} códigos en Sector 3...`);
    
    let insertados = 0;
    let errores = 0;
    
    for (const codigo of codigosMotores) {
      try {
        const id = `motor-${codigo}-${Date.now()}-${Math.random().toString(36).slice(2, 7)}`;
        
        // Insertar nuevo motor
        await client.query(
          `INSERT INTO motores (id, sector, codigo, estado_motor, piscina_id, fecha_registro)
           VALUES ($1, $2, $3, $4, $5, NOW())`,
          [id, 'Sector 3', codigo, 'Taller por Mantenimiento', null]
        );
        insertados++;
        console.log(`✅ Insertado: ${codigo}`);
      } catch (error) {
        errores++;
        console.error(`❌ Error con código ${codigo}:`, error.message);
      }
    }
    
    console.log('\n=== RESUMEN ===');
    console.log(`Total a insertar: ${codigosMotores.length}`);
    console.log(`Insertados exitosamente: ${insertados}`);
    console.log(`Errores: ${errores}`);
    
  } catch (error) {
    console.error('Error general:', error);
  } finally {
    client.release();
    await pool.end();
  }
}

addMotoresSector3();
